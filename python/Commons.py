import requests
import hashlib
import random


def make_get_request(url, payload=None, headers=None, is_json=True):
    req = requests.get(
        url,
        params=payload,
        headers=headers and headers or {}
    )

    if 200 >= req.status_code <= 299:
        if is_json:
            return req.json()
        else:
            return req.content
    else:
        return None


